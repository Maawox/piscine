int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int y;
	int nb;

	nb = 0;
	y = 0;
	while (y < length)
	{
		if ((*f)(tab[y]) != 0)
			nb++;
		y++;
	}
	return (nb);
}
