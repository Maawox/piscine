void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int buffer;

	j = size - 1;
	i = 0;
	while (i < j)
	{
		buffer = tab[i];
		tab[i] = tab[j];
		tab[j] = buffer;
		i++;
		j--;
	}
}
