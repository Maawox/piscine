int		ft_str_is_alpha(char *str)
{
	int i;
	int check;

	check = 1;
	i = 0;
	while (str[i])
	{
		if ((!(str[i] >= 'A' && str[i] <= 'Z'))
			&& (!(str[i] >= 'a' && str[i] <= 'z')))
			check = 0;
		i++;
	}
	return (check);
}
