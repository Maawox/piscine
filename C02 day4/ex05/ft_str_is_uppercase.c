int		ft_str_is_uppercase(char *str)
{
	int i;
	int check;

	i = 0;
	check = 1;
	while (str[i])
	{
		if (!(str[i] >= 65 && str[i] <= 90))
			check = 0;
		i++;
	}
	return (check);
}
