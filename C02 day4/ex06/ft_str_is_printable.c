int		ft_str_is_printable(char *str)
{
	int i;
	int check;

	check = 1;
	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 32 && str[i] <= 127))
			check = 0;
		i++;
	}
	return (check);
}
